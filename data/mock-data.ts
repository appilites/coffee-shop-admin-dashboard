export interface Product {
  id: string
  name: string
  description: string | null
  price: number
  imageUrl: string | null
  categoryId: string
  category: {
    name: string
    emoji: string
    parent_id?: string | null
  }
  categoryPath?: string
  isAvailable: boolean
  isFeatured: boolean
  sku: string | null
  createdAt: Date
}

export interface Category {
  id: string
  name: string
  emoji: string
  description: string | null
  slug: string
  isActive: boolean
  _count: {
    products: number
  }
}

export interface Order {
  id: string
  orderNumber: string
  customerName: string
  customerEmail: string
  customerPhone: string | null
  totalAmount: number
  status: "PENDING" | "CONFIRMED" | "PREPARING" | "READY" | "COMPLETED" | "CANCELLED"
  paymentStatus: "paid" | "pending" | "failed"
  locationId: string | null
  specialNotes: string | null
  createdAt: Date
  items: Array<{
    id: string
    quantity: number
    price: number
    customizations?: string | null
    product: {
      id: string
      name: string
      imageUrl?: string | null
    }
  }>
}

